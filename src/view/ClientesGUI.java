    private Integer Conversao(String palavra){
        Pattern pattern = Pattern.compile("^(\\d+),\\s*.*$");
        Matcher matcher = pattern.matcher(palavra);
        
        if (matcher.find()) {
        String idString = matcher.group(1);
        Integer id = Integer.parseInt(idString);
        return id;
    }
        return null;
    } 

   
